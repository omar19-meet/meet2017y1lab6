import turtle

turtle.shape('turtle')
square=turtle.clone()
square.shape('square')
square.goto(100,100)
triangle=turtle.clone()
triangle.shape('triangle')
square.goto(0,100)
square.stamp()
square.goto(100,100)
square.stamp()
square.goto(100,0)
square.goto(0,0)
triangle.goto(50,50)
triangle.goto(100,0)
triangle.goto(0,0)
square.goto(300,300)
square.stamp()
square.goto(100,100)
triangle.goto(-500,-500)
triangle.stamp()
triangle.goto(-100,-100)

turtle.mainloop()

